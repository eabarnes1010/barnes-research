#!/usr/bin/env python3
"""Build the home-page Publications word cloud from paper titles.

Reads _bibliography/papers.bib, counts informative words across all titles
(once per paper), and writes _data/pub_wordcloud.yml — a ranked list of
{word, tier} that _layouts/home.liquid renders as a packed tag cloud.

Re-run after editing papers.bib:  python3 bin/build_pub_wordcloud.py
(Or wire it into the build/CI so the cloud always reflects the latest bib.)

Curation lives in the two lists below: STOPWORDS (dropped) and ACRONYMS
(shown uppercase). Edit those to tune what surfaces.
"""

import re
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
BIB = ROOT / "_bibliography" / "papers.bib"
OUT = ROOT / "_data" / "pub_wordcloud.yml"

TOP_N = 16          # words shown in the cloud
TIERS = 5           # size buckets (1 = largest)
MIN_LEN = 3         # ignore words shorter than this (unless an acronym)

# Words shown in UPPERCASE (domain acronyms). Compared case-insensitively.
ACRONYMS = {
    "ai", "ml", "enso", "mjo", "sst", "s2s", "cnn", "nao", "qbo", "us",
    "ai2es", "noaa", "sai", "tc", "tcs",
}

# Generic / structural words to drop. Lowercase.
STOPWORDS = {
    # articles, prepositions, conjunctions, pronouns
    "the", "a", "an", "and", "or", "of", "in", "on", "for", "to", "with",
    "from", "by", "at", "as", "is", "are", "be", "into", "over", "under",
    "between", "across", "during", "without", "within", "via", "toward",
    "towards", "their", "its", "our", "we", "that", "this", "these", "those",
    "than", "but", "not", "can", "does", "do", "how", "what", "when", "why",
    "which", "using", "use", "uses", "based", "case", "study", "new", "more",
    "less", "two", "three", "one", "first", "role", "potential", "effect",
    "effects", "impact", "impacts", "evidence", "future",
    # generic research verbs/nouns that add little signal in a cloud
    "assessing", "identifying", "improving", "comparing", "exploring",
    "understanding", "evaluating", "evaluation", "application", "applications",
    "approach", "approaches", "analysis", "analyses", "method", "methods",
    "framework", "results", "toward", "insights",
    # generic in this corpus — surfaced but not informative as a topic
    "model", "models", "surface", "sea", "change", "changes",
    "response", "responses",
}


def extract_titles(text):
    """Return the brace-balanced contents of every `title = { ... }` field."""
    titles = []
    for m in re.finditer(r"\btitle\s*=\s*\{", text, re.IGNORECASE):
        start = m.end()
        depth, j = 1, start
        while j < len(text) and depth:
            depth += {"{": 1, "}": -1}.get(text[j], 0)
            j += 1
        titles.append(text[start : j - 1])
    return titles


def clean(title):
    """Strip LaTeX commands and braces, leaving plain text."""
    title = re.sub(r"\\[a-zA-Z]+", " ", title)   # \textit, \~ -> drop command
    title = re.sub(r"[{}\\~^]", " ", title)       # leftover braces/diacritics
    return title


def tokens(title):
    """Unique informative words in one title (deduped per paper)."""
    seen = {}
    for tok in re.findall(r"[A-Za-z][A-Za-z0-9-]*", clean(title)):
        key = tok.lower().strip("-")
        if not key or key in STOPWORDS:
            continue
        if len(key) < MIN_LEN and key not in ACRONYMS:
            continue
        seen.setdefault(key, tok)  # remember first surface form (unused now)
    return seen.keys()


def display(word):
    return word.upper() if word in ACRONYMS else word


def main():
    if not BIB.exists():
        sys.exit(f"bib not found: {BIB}")
    titles = extract_titles(BIB.read_text(encoding="utf-8"))
    counts = Counter()
    for t in titles:
        counts.update(tokens(t))
    if not counts:
        sys.exit("no words found")

    top = counts.most_common(TOP_N)
    n = len(top)

    lines = [
        "# Auto-generated by bin/build_pub_wordcloud.py — do not edit by hand.",
        "# Re-run that script after updating _bibliography/papers.bib.",
        f"# Source: {len(titles)} paper titles.",
    ]
    # Rank-based tiers: split the ranked words into TIERS even buckets so the
    # cloud has visual variety even when one word dominates the raw counts.
    for i, (word, c) in enumerate(top):
        tier = 1 + min(TIERS - 1, i * TIERS // n)
        lines.append(f"- word: {display(word)}")
        lines.append(f"  tier: {tier}")
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {OUT.relative_to(ROOT)} — {len(top)} words from {len(titles)} titles")


if __name__ == "__main__":
    main()
