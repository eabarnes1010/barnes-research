#!/usr/bin/env python3
"""Build the home-page Publications "papers per year" data from papers.bib.

Reads _bibliography/papers.bib, counts one paper per @entry by its `year`
field, and writes _data/pub_peryear.yml — a chronological list of
{year, count} that _layouts/home.liquid renders as a hand-drawn (rough.js)
bar chart in the Publications feature card.

Years with no papers inside the range are emitted with count 0 so the bar
chart shows a true, gap-aware timeline.

Re-run after editing papers.bib:  python3 bin/build_pub_peryear.py
(Or wire it into the build/CI so the chart always reflects the latest bib.)
"""

import re
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
BIB = ROOT / "_bibliography" / "papers.bib"
OUT = ROOT / "_data" / "pub_peryear.yml"

# Only show this many most-recent years in the card (keeps the small 16:9
# tile legible). Set to 0 to show every year present in the bib.
RECENT_YEARS = 12


def entry_years(text):
    """Yield the year of every @entry (one per bibtex record)."""
    # Walk each @type{ ... } record with brace matching, then read its year.
    for m in re.finditer(r"@\w+\s*\{", text):
        start = m.end()
        depth, j = 1, start
        while j < len(text) and depth:
            depth += {"{": 1, "}": -1}.get(text[j], 0)
            j += 1
        block = text[start : j - 1]
        ym = re.search(r"\byear\s*=\s*[{\"]?\s*(\d{4})", block, re.IGNORECASE)
        if ym:
            yield int(ym.group(1))


def main():
    if not BIB.exists():
        sys.exit(f"bib not found: {BIB}")
    years = list(entry_years(BIB.read_text(encoding="utf-8")))
    if not years:
        sys.exit("no years found")
    counts = Counter(years)
    lo, hi = min(years), max(years)
    full = [(y, counts.get(y, 0)) for y in range(lo, hi + 1)]
    if RECENT_YEARS:
        full = full[-RECENT_YEARS:]

    lines = [
        "# Auto-generated by bin/build_pub_peryear.py — do not edit by hand.",
        "# Re-run that script after updating _bibliography/papers.bib.",
        f"# Source: {len(years)} papers, {lo}-{hi}.",
    ]
    for y, c in full:
        lines.append(f"- year: {y}")
        lines.append(f"  count: {c}")
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {OUT.relative_to(ROOT)} — {len(full)} years "
          f"({full[0][0]}-{full[-1][0]}), {len(years)} papers total")


if __name__ == "__main__":
    main()
